import { execFileSync } from "node:child_process";
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { jsonSchemaToZod } from "json-schema-to-zod";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const webRoot = resolve(scriptDir, "..");
const repoRoot = resolve(webRoot, "../..");
const outputPath = resolve(webRoot, "src/lib/generated/schemas.ts");
const checkMode = process.argv.includes("--check");

const rawSchemas = execFileSync(
  "cargo",
  ["run", "--quiet", "-p", "shared", "--bin", "export_web_schemas"],
  { cwd: repoRoot, encoding: "utf8" },
);
const schemas = JSON.parse(rawSchemas);

let output = `// @generated by \`bun run generate:schemas\`. Do not edit manually.\n`;
output += `import { z } from "zod";\n\n`;

for (const [schemaName, schema] of Object.entries(schemas)) {
  const resolvedSchema = stripJsonSchemaMetadata(
    preserveStringConstraintsWithNot(resolveLocalRefs(schema, schema)),
  );
  output += `${jsonSchemaToZod(resolvedSchema, {
    name: schemaName,
    module: "esm",
    noImport: true,
  })};\n\n`;
}

for (const schemaName of Object.keys(schemas).sort()) {
  output += `export type ${schemaNameToTypeName(schemaName)} = z.infer<typeof ${schemaName}>;\n`;
}
output += `export type AdminChallengeListItem = AdminChallengeListResponse["items"][number];\n`;
output += `export type ChallengeDraftListItem = ChallengeDraftListResponse["items"][number];\n`;
output += `export type AdminSolutionSubmissionListItem = AdminSolutionSubmissionListResponse["items"][number];\n`;
output = replaceRegexConstructors(output);
output = formatTypescript(output);

mkdirSync(dirname(outputPath), { recursive: true });
if (checkMode) {
  const existing = readFileSync(outputPath, "utf8");
  if (existing !== output) {
    throw new Error(
      "generated schemas are stale; run `bun run generate:schemas` in frontends/web",
    );
  }
} else {
  writeFileSync(outputPath, output);
}

function resolveLocalRefs(node, root) {
  if (Array.isArray(node)) {
    return node.map((item) => resolveLocalRefs(item, root));
  }
  if (!node || typeof node !== "object") {
    return node;
  }
  if (typeof node.$ref === "string") {
    return resolveLocalRefs(resolvePointer(root, node.$ref), root);
  }

  const resolved = {};
  for (const [key, value] of Object.entries(node)) {
    if (key === "$defs") {
      continue;
    }
    resolved[key] = resolveLocalRefs(value, root);
  }
  return resolved;
}

function resolvePointer(root, pointer) {
  if (!pointer.startsWith("#/")) {
    throw new Error(`unsupported non-local JSON Schema ref: ${pointer}`);
  }

  return pointer
    .slice(2)
    .split("/")
    .map((part) => part.replaceAll("~1", "/").replaceAll("~0", "~"))
    .reduce((current, part) => {
      if (!current || typeof current !== "object" || !(part in current)) {
        throw new Error(`unresolved JSON Schema ref: ${pointer}`);
      }
      return current[part];
    }, root);
}

function stripJsonSchemaMetadata(node) {
  if (Array.isArray(node)) {
    return node.map(stripJsonSchemaMetadata);
  }
  if (!node || typeof node !== "object") {
    return node;
  }

  const cleaned = {};
  for (const [key, value] of Object.entries(node)) {
    if (key === "$schema") {
      continue;
    }
    cleaned[key] = stripJsonSchemaMetadata(value);
  }
  return cleaned;
}

function preserveStringConstraintsWithNot(node) {
  if (Array.isArray(node)) {
    return node.map(preserveStringConstraintsWithNot);
  }
  if (!node || typeof node !== "object") {
    return node;
  }

  const rewritten = {};
  for (const [key, value] of Object.entries(node)) {
    rewritten[key] = preserveStringConstraintsWithNot(value);
  }

  if (rewritten.type === "string" && rewritten.not !== undefined) {
    const { not, ...baseStringSchema } = rewritten;
    return {
      allOf: [baseStringSchema, { not }],
    };
  }

  return rewritten;
}

function replaceRegexConstructors(source) {
  return source.replaceAll(/new RegExp\("((?:\\.|[^"\\])*)"\)/g, (_, raw) => {
    const pattern = JSON.parse(`"${raw}"`);
    return `/${escapeRegexLiteralSlashes(pattern)}/`;
  });
}

function escapeRegexLiteralSlashes(pattern) {
  let output = "";
  let escaped = false;
  let inCharacterClass = false;
  for (const char of pattern) {
    if (escaped) {
      output += char;
      escaped = false;
      continue;
    }
    if (char === "\\") {
      output += char;
      escaped = true;
      continue;
    }
    if (char === "[") {
      inCharacterClass = true;
    } else if (char === "]") {
      inCharacterClass = false;
    }
    output += char === "/" && !inCharacterClass ? "\\/" : char;
  }
  return output;
}

function formatTypescript(source) {
  return execFileSync(
    "bunx",
    ["biome", "format", "--stdin-file-path", outputPath],
    {
      cwd: webRoot,
      input: source,
      encoding: "utf8",
    },
  );
}

function schemaNameToTypeName(schemaName) {
  if (!schemaName.endsWith("Schema")) {
    throw new Error(`schema export name must end with Schema: ${schemaName}`);
  }
  const baseName = schemaName.slice(0, -"Schema".length);
  return `${baseName[0].toUpperCase()}${baseName.slice(1)}`;
}
